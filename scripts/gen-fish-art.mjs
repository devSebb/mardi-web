#!/usr/bin/env node
/**
 * One-shot sampler: reads public/assets/MardiFish.png and emits the fish
 * silhouette as a braille-art multiline string in app/_og/fish-art.ts.
 *
 *   npm run gen:fish-art
 *
 * Re-run if the source PNG changes. The OG image consumes the baked constant —
 * it can't decode a PNG at edge-render time.
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { PNG } from "pngjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

// Render target.
const COLS = 40;  // braille cells horizontally
const ROWS = 20;  // braille cells vertically
const DOT_COLS = COLS * 2;   // 80
const DOT_ROWS = ROWS * 4;   // 80

// Luminance threshold for "this dot is lit". Matches the Swift sampler.
const LUM_MIN = 0.12;

// Braille dot layout: (colOffset, rowOffset, bit in U+2800 codepoint).
const LAYOUT = [
  [0, 0, 0x01], [1, 0, 0x08],
  [0, 1, 0x02], [1, 1, 0x10],
  [0, 2, 0x04], [1, 2, 0x20],
  [0, 3, 0x40], [1, 3, 0x80],
];

// ── Decode the PNG ────────────────────────────────────────────────────
const srcPath = resolve(ROOT, "public/assets/MardiFish.png");
const png = PNG.sync.read(readFileSync(srcPath));
const { width, height, data } = png;

// ── Sample into dot grid ─────────────────────────────────────────────
const dots = new Uint8Array(DOT_COLS * DOT_ROWS);
for (let dy = 0; dy < DOT_ROWS; dy++) {
  for (let dx = 0; dx < DOT_COLS; dx++) {
    const sx = Math.min(Math.floor((dx / DOT_COLS) * width),  width  - 1);
    const sy = Math.min(Math.floor((dy / DOT_ROWS) * height), height - 1);
    const off = (sy * width + sx) * 4;
    const r = data[off], g = data[off + 1], b = data[off + 2], a = data[off + 3];
    const lum = (0.299 * r + 0.587 * g + 0.114 * b) / 255;
    dots[dy * DOT_COLS + dx] = a > 20 && lum > LUM_MIN ? 1 : 0;
  }
}

// ── Pack 2×4 dot blocks into braille glyphs ──────────────────────────
// Empty cells are emitted as ASCII space (U+0020), not U+2800. Some renderers
// (notably Satori's default font in next/og) paint U+2800 as a visible empty
// dot-matrix, which turns negative space into a visible grid. A plain space
// has the same monospace advance-width, so the silhouette stays aligned.
const rows = [];
for (let row = 0; row < ROWS; row++) {
  let line = "";
  for (let col = 0; col < COLS; col++) {
    const dotX = col * 2;
    const dotY = row * 4;
    let cp = 0x2800;
    for (const [dc, dr, bit] of LAYOUT) {
      const sx = dotX + dc;
      const sy = dotY + dr;
      if (dots[sy * DOT_COLS + sx]) cp |= bit;
    }
    line += cp === 0x2800 ? " " : String.fromCodePoint(cp);
  }
  rows.push(line);
}

// ── Emit the TS module ───────────────────────────────────────────────
const outDir  = resolve(ROOT, "app/_og");
const outFile = resolve(outDir, "fish-art.ts");
mkdirSync(outDir, { recursive: true });

const banner = `// AUTO-GENERATED by scripts/gen-fish-art.mjs — do not edit by hand.
// Source: public/assets/MardiFish.png  (${width}×${height})
// Grid:   ${COLS} cols × ${ROWS} rows  (${DOT_COLS}×${DOT_ROWS} dots)
`;
const body = `${banner}
export const FISH_COLS = ${COLS};
export const FISH_ROWS = ${ROWS};

export const FISH_ART: readonly string[] = [
${rows.map((r) => `  ${JSON.stringify(r)},`).join("\n")}
];
`;
writeFileSync(outFile, body, "utf8");

const lit = Array.from(dots).reduce((s, v) => s + v, 0);
console.log(
  `✓ wrote ${outFile}  (${rows.length} rows, ${lit}/${DOT_COLS * DOT_ROWS} dots lit)`
);
